
public class Animal {
	// Variables del Animal
	String nombre;
	int edad;
	
	// Definicion de Animal y sus variables por Animal
	Animal(String nombre, int edad)
	{
		this.nombre = nombre;
		this.edad = edad;
	}
	
	// Retornar variable nombre
	String getNombre()
	{
		return nombre;
	}
	
	// Retornar variable edad
	int getEdad()
	{
		return edad;
	}
	
	// Asigna y define la variable nombre
	void setNombre(String nombre)
	{
		this.nombre = nombre;
	}
	// Asigna y define la variable edad
	void setEdad(int edad)
	{
		this.edad = edad;
	}

	
	
	
}
